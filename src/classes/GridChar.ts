    constructor(pChar : string, pColour : string | undefined)
    {
        this.#fGrid = new Grid(5, 7);

        const lBlockTypeRandom : BlockType = Block.getRandomType(); //lBlockTypes[utils.getRandomInt(0, lBlockTypes.length - 1)];

        this.#fColourRandom = Block.sColoursMap.get(lBlockTypeRandom) ?? "#00000000"; //[lBlockTypeRandom];

        let lColor : string | undefined = pColour;

        if (!lColor)
        {    
            lColor = this.#fColourRandom;
        }

        if (!GridChar.sCharDefs5x7.has(pChar))
        {
            pChar = '?';
        }

        const lCharDef : string[] | undefined = GridChar.sCharDefs5x7.get(pChar);

        if (lCharDef)
        {
            for (let i = 0; i < lCharDef.length; ++i)
                {
                    if (lCharDef[i] == '1')
                    {
                        this.#fGrid.grid[i].backgroundColour = lColor;
                    }
                }
        }
    }

    get grid()
    {
        return this.#fGrid;
    }

